    private void luubpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_luubpActionPerformed
        String MBP=mbp.getText();
        String TBP=tbp.getText();
        int temp0=0,temp1=0;
        int check=kiemTraBP(MBP);

        if(MBP.equals("")){
            temp0=1;
            thongbaobophan.setForeground(Color.RED);
            thongbaobophan.setText("Mã Không Được Bỏ Trống ");
        }
        else if(TBP.equals("")){
            temp1=1;
            thongbaobophan.setForeground(Color.RED);
            thongbaobophan.setText("Tên  Không Được Bỏ Trống ");
        }

        else if(temp0!=1 &&temp1!=1 ){
            if(check==1){
                SuaBP(MBP);
                layBP();
                thongbaobophan.setForeground(Color.BLUE);
                thongbaobophan.setText("Sửa Thành Công ");
            }
            else{
                LuuBP(MBP,TBP);
                thongbaobophan.setForeground(Color.BLUE);
                thongbaobophan.setText("Lưu Thành Công ");
                layBP();
            }
        }
    }//GEN-LAST:event_luubpActionPerformed

    private void xoabpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_xoabpActionPerformed
        String MBP =mbp.getText();
        int check;
        int kiemTra = 0;
        if (MBP.equals("")) {
            kiemTra = 1;
            JOptionPane.showMessageDialog(this, "Hãy nhập Mã  bạn muốn xóa");
        }
        else if (kiemTra != 1) {
            check = kiemTraBP(MBP);
            if (check == 1) {

                int ret = JOptionPane.showConfirmDialog(this, "Bạn Chắc Chắn Muốn Xóa?", "Xác Nhận", 0);
                if(ret == JOptionPane.CANCEL_OPTION)
                return;
                else  if(ret == JOptionPane.OK_OPTION)
                {
                    xoaBP(MBP);
                    JOptionPane.showMessageDialog(this, "Xóa  thành công");
                    this.layBP();

                }
            }
            else {
                JOptionPane.showMessageDialog(this, "Mã Chưa Có");
            }
        }
    }//GEN-LAST:event_xoabpActionPerformed

